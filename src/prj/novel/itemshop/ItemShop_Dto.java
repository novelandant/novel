package prj.novel.itemshop;

public class ItemShop_Dto {
	
	private int pur_num;
	private String member_id;
	private String pur_date;
	private String pur_way;
	private int sum;
	private String util_kind;
	
	
	
	
	
	public int getPur_num() {
		return pur_num;
	}
	public void setPur_num(int pur_num) {
		this.pur_num = pur_num;
	}
	public String getMember_id() {
		return member_id;
	}
	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}
	public String getPur_date() {
		return pur_date;
	}
	public void setPur_date(String pur_date) {
		this.pur_date = pur_date;
	}
	public String getPur_way() {
		return pur_way;
	}
	public void setPur_way(String pur_way) {
		this.pur_way = pur_way;
	}
	public int getSum() {
		return sum;
	}
	public void setSum(int sum) {
		this.sum = sum;
	}
	public String getUtil_kind() {
		return util_kind;
	}
	public void setUtil_kind(String util_kind) {
		this.util_kind = util_kind;
	}
	
}
